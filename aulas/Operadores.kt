package br.com.kotlin.aulas

fun main() {
    // Soma
    println("10 + 3 = ${10 + 3}")

    // Subtração
    println("10 - 3 = ${10 - 3}")

    // Multiplicação
    println("10 * 3 = ${10 * 3}")

    // Divisão
    println("10 / 3 = ${10 / 3}")

    // Resto da Divisão
    println("10 % 3 = ${10 % 3}")

    // Igual
    println("10 == 3? ${10 == 3}")

    // Maior
    println("10 > 3? ${10 > 3}")

    // Menor
    println("10 < 3? ${10 < 3}")

    // Maior ou Igual
    println("10 >= 3? ${10 >= 3}")

    // Menor ou Igual
    println("10 <= 3? ${10 <= 3}")
}